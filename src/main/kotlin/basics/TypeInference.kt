package basics

fun main() {
    // No need to specifically write type when declaring variables and assigning values
    // Kotlin infers the type automatically for every variable
    var i = 21  // Int
    var l = 100L // Long
    var d = 3.14  // Double
    var f = 3.14F // Float
    var b = true // Boolean
    var s = "Jetbrains" // String
    var c = 'K' // Char
}